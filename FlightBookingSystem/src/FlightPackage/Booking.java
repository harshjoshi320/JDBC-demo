    private void FindBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FindBTNActionPerformed
        // TODO add your handling code here:
        if (!isDateValid(datetxt.getText())) {
            JOptionPane.showMessageDialog(this, "Invalid Date", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            DefaultComboBoxModel dmodel = (DefaultComboBoxModel) AvlFlights.getModel();
            dmodel.removeAllElements();
            SimpleDateFormat Dform = new SimpleDateFormat("dd/MM/yyyy");
            try {
                Date curD = Dform.parse(datetxt.getText());
                Calendar cal = Calendar.getInstance();
                cal.setTime(curD);
                int day = cal.get(Calendar.DAY_OF_WEEK);// gets day of week             
// Connection

                String CmdSql = "select flightno,from_f,to_f from flights where flight_day like '%" + day + "%' ;";

                ResultSet rs = connect().executeQuery(CmdSql);
// Sets the model of combobox
                while (rs.next()) {
                    dmodel.addElement(rs.getString("flightno") + "," + rs.getString("from_f") + "-" + rs.getString("to_f"));

                }
                AvlFlights.setModel(dmodel);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_FindBTNActionPerformed

