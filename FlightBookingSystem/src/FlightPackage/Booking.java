    private void BookBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BookBTNActionPerformed
        // TODO add your handling code here:
// Checks the validity of the date        
        if (!isDateValid(datetxt.getText())) {
            JOptionPane.showMessageDialog(this, "Invalid Date", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
       
// Creating new date format        
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        try {
            Date date1 = sdf.parse(datetxt.getText()); // Gets input date
            Date cdate = new Date(); // Gives System date
// Comparing input date to system date           
            if (date1.compareTo(cdate) <= 0) {
                JOptionPane.showMessageDialog(this, "Booking not allowed before current date", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Booking not allowed before current date", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
// Comparing input no of seats with available seats.         
        BookingVar.NuSeats = Integer.parseInt(No_seats.getText());
        if (BookingVar.NuSeats > BookingVar.SeatsAvl) {
            JOptionPane.showMessageDialog(this, "Number of seats can't be more than available seats", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (BookingVar.NuSeats == 0) {
            JOptionPane.showMessageDialog(this, "Please enter the number of seats to be booked", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
// Connection and update command        
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/flight_book", "root", "ipqwe320");
            Statement stmt = con.createStatement();
            String CmdSql = "insert into booking (idno,user_name,flightno,date_flight,date_book,time_book,class,tickets,rate) values(" + UserGlobal.userID + ",'" + UserGlobal.userName + "'," + BookingVar.flightno + ",str_to_date('" + datetxt.getText() + "', '%d/%m/%Y'),now(),now(),'" + ClassCmb.getSelectedItem().toString() + "'," + BookingVar.NuSeats + "," + BookingVar.Rate + ")";
// Checks whether update command has been successfully executed or not. 

            int rowAff = stmt.executeUpdate(CmdSql, Statement.RETURN_GENERATED_KEYS);
            if (rowAff == 0) {
                JOptionPane.showMessageDialog(this, "An error occured while booking please try again.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            } else {

                ResultSet keys = stmt.getGeneratedKeys();
                keys.next();
                JOptionPane.showMessageDialog(this, "Ticket Booked! PNR No.:" + keys.getInt(1), "Success", JOptionPane.INFORMATION_MESSAGE);

                new MainForm().setVisible(true);
                this.dispose();
            }




        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_BookBTNActionPerformed

