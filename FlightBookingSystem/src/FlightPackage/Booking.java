    private void ClassCmbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ClassCmbActionPerformed
        // TODO add your handling code here:
        try {
           



            String Ticketcmd = "select coalesce(sum(tickets),0) as tsold from booking where flightno = " + BookingVar.flightno + " and  class = '" + ClassCmb.getSelectedItem().toString() + "' and date_format(date_flight,'%d/%m/%Y')='" + datetxt.getText() + "';";


            ResultSet rs1 = connect().executeQuery(Ticketcmd);
            rs1.next();


            String cancTick = "select sum(tickets) from booking where canceled = 'Y' and flightno = " + BookingVar.flightno + ";";


            ResultSet rs2 = connect().executeQuery(cancTick);
            rs2.next();

            int Ticketsold = rs1.getInt("tsold");
            int TicketCancel = rs2.getInt("sum(tickets)");


            if (ClassCmb.getSelectedItem().equals("Economy")) {
                BookingVar.SeatsAvl = BookingVar.ecoSeats - Ticketsold + TicketCancel;
                BookingVar.Rate = BookingVar.ecoRate;
            } else if (ClassCmb.getSelectedItem().equals("Bussiness")) {
                BookingVar.Rate = BookingVar.bussRate;
                BookingVar.SeatsAvl = BookingVar.bussSeats - Ticketsold + TicketCancel;
            } else if (ClassCmb.getSelectedItem().equals("First Class")) {
                BookingVar.Rate = BookingVar.firstRate;
                BookingVar.SeatsAvl = BookingVar.firstSeats - Ticketsold + TicketCancel;
            }
            SeatLbl.setText(BookingVar.SeatsAvl + "");


        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_ClassCmbActionPerformed

