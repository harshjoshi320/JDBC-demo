    public static boolean isDateValid(String date) {
        // isDateValid Method created - Checks validity of the input date
        try {
            DateFormat Df = new SimpleDateFormat("dd/MM/yyyy");
            Df.setLenient(false);
            Df.parse(date);
            return true;
        } catch (ParseException e) {
            return false;
        }
    }

